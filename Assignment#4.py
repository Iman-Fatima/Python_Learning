# Create a Mini Calculator that can operate on two operands and can perform:
#     Addition
#     Subtraction
#     Multiplication
#     Division
#     Floor Division
#     Exponentiation
#     # Modulus

# --> ADDITION:
number_1 = 5
number_2 = 10
number_3 = number_1 + number_2
print(f'The sum of the given numbers are: {number_3}')
# The sum of the given numbers are: 15


# --> SUBTRACTION:

number_3 = number_1 - number_2
print(f'The subtraction of the given numbers are: {number_3}')
# The subtraction of the given numbers are: 15



# --> MULTIPLICATION:

number_3 = number_1 * number_2
print(f'The multiplication of the given numbers are: {number_3}')
# The multiplication of the given numbers are: 50


# --> DIVISION:
number_1 = 10
number_2 = 5
number_3 = number_1 / number_2
print(f'The division of the given numbers are: {number_3}')
# The division of the given numbers are: 2.0


# --> FLOOR DIVISION:
number_1 = 10
number_2 = 5
number_3 = number_1 // number_2
print(f'The floor division of the given numbers are: {number_3}')
# The floor division of the given numbers are: 2

# --> EXPONENTIATION:
number_1 = 5
number_2 = 4
number_3 = number_1 ** number_2
print(f'The exponentiation of the given numbers are: {number_3}')
# The exponentiation of the given numbers are: 625



# --> MODULUS:
number_1 = 6
number_2 = 4
number_3 = number_1 % number_2
print(f'The modulus of the given numbers are: {number_3}')
# The modulus of the given numbers are: 2


