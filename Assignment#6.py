# ---> Equal to:-
operand_1 = 5
operand_2 = 5
result = (operand_1 == operand_2)
print(f"The given operands {operand_1} and {operand_2} are equal (==) to each other : {result}")
# <--------------- OUTPUT --------------->  
# The given operands 5 and 5 are equal (==) to each other : True


# ---> Greater than or equal to:-
operand_1 = 4
operand_2 = 5
result = (operand_1 >= operand_2)
print(f"The given operands {operand_1} and {operand_2} are greater than or equal (>=) to each other : {result}")
# <--------------- OUTPUT --------------->  
# The given operands 4 and 5 are greater than or equal (>=) to each other : False

